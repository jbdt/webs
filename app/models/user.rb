class User < ApplicationRecord
  devise :database_authenticatable, :registerable,
         :recoverable, :rememberable, :validatable

  enum role: [ :admin, :integrator, :client ]

  private

  def admin?
    self.role == 'admin'
  end

  def integrator?
    self.role == 'integrator'
  end

  def client?
    self.role == 'client'
  end

end
